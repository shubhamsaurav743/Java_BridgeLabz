public class PrimitiveDataTypes
{
 static int value1;
 static float value2;
 static boolean value3;
 static long value4;
 static char value5;
 static byte value6;
 static short value7;
 static double value8;
 public static void main(String[] args)
 {
  System.out.println("Default value of integer : "+value1);
  System.out.println("Default value of float : "+value2);
  System.out.println("Default value of boolean : "+value3);
  System.out.println("Default value of long : "+value4);
  System.out.println("Default value of char : "+value5);
  System.out.println("Default value of byte :  "+value6);
  System.out.println("Default value of short : "+value7);
  System.out.println("Default value of double : "+value8);
 }
}
